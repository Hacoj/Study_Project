package lesson.warpper_.warpper;

public class WarpperMethods {
    public static void main(String[] args) {
        System.out.println(Integer.MAX_VALUE);//最大值
        System.out.println(Integer.MIN_VALUE);//最小值

        System.out.println(Character.isDigit('a'));//是否数字
        System.out.println(Character.isLetter('a'));//是否字母
        System.out.println(Character.isUpperCase('a'));//是否大些
        System.out.println(Character.isLowerCase('a'));//是否小写
        System.out.println(Character.isWhitespace('a'));//是否空格
        System.out.println(Character.toUpperCase('a'));//转成大写
        System.out.println(Character.toLowerCase(('A')));//转成小写
    }
}
